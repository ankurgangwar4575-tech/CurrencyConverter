const selectOptions = document.querySelectorAll("select");
for (let select of selectOptions) {
  for (let key in countryList) {
    let opt = document.createElement("option");
    opt.value = key;
    opt.innerText = key;
    select.append(opt);
    if (
      (select.name == "from" && key == "USD") ||
      (select.name == "to" && key == "INR")
    )
      opt.selected = "selected";
    select.addEventListener("change", (evt) => {
      flags(evt.target);
    });
  }
}
const flags = (el) => {
  let countryCode = el.value;
  let flagCode = countryList[countryCode];
  let newLink = `https://flagsapi.com/${flagCode}/flat/64.png`;
  let img = el.parentElement.querySelector("img");
  img.src = newLink;
};
