const B_URL =
  "https://cdn.jsdelivr.net/npm/@fawazahmed0/currency-api@latest/v1/currencies";
const but = document.querySelector("button");
const to = document.querySelector(".to select");
const from = document.querySelector(".from select");
const msg = document.querySelector(".msg");
const icon = document.querySelector("i");
icon.addEventListener("click", () => {
  let tempValue = from.value;
  from.value = to.value;
  to.value = tempValue;

  flags(from);
  flags(to);
  msg.style.visibility = "hidden";
});
but.addEventListener("click", async (evt) => {
  evt.preventDefault();
  let amount = document.querySelector(".input-amount ").value.trim();
  const URL = `${B_URL}/${from.value.toLowerCase()}.json`;
  try {
    let response = await fetch(URL);
    let data = await response.json();
    console.log(data);
    let rate = data[from.value.toLowerCase()][to.value.toLowerCase()];
    let totalAmount = amount * rate;
    msg.style.visibility = "visible";
    msg.innerText = `${amount} ${from.value} = ${totalAmount} ${to.value}`;
  } catch (error) {
    console.error("Error fetching data:", error);
    msg.innerText = "Failed to fetch exchange rate.";
  }
});

